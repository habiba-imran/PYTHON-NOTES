# MATHEMATICL EXPRESSIONS ON NUMPPY ARRAY:
# allows us to traverse through all elements withput actually looping
import numpy as np
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
print(a + b) # sum
print(a - b) # difference
print(b - a) # difference
print(a / b) # for float division
print(a // b) # for integer division
print(a * b) # [ 4 10 18]
print(a % b) # modulus [1 2 3]
print(a ** 2) # every element ^ 2





